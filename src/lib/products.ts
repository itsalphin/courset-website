export function getProductsByCollection(collection: Product['collection']): Product[] {
  return products.filter((p) => p.collection === collection);
}

export function getProductById(id: string): Product | undefined {
  return products.find((p) => p.id === id);
}

export function getComplementaryProducts(productId: string): Product[] {
  const product = getProductById(productId);
  if (!product) return [];
  return product.complementaryIds
    .map((id) => getProductById(id))
    .filter((p): p is Product => p !== undefined);
}

export function getProductsByOccasion(occasion: string): Product[] {
  if (occasion === 'all') return products;
  return products.filter((p) => p.occasions.includes(occasion));
}
